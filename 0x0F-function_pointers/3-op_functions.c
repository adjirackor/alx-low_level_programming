/**
  * op_add - returns sum of two integers
  * @a: fist integer
  * @b: second integer
  *
  * Return: sum of a and b
  */
int op_add(int a, int b)
{
	return (a + b);
}

/**
  * op_sub - returns the difference between two integers
  * @a: fist integer
  * @b: second integer
  *
  * Return: difference between a and b
  */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
  * op_mul - returns product of two integers
  * @a: fist integer
  * @b: second integer
  *
  * Return: product of a and b
  */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
  * op_div - returns the result of the division  of two integers
  * @a: fist integer
  * @b: second integer
  *
  * Return: result of division of a and b
  */
int op_div(int a, int b)
{
	return (a / b);
}
/**
  * op_mod - returns the remainder of the division of two integers
  * @a: fist integer
  * @b: second integer
  *
  * Return: remainder of the division of a and b
  */
int op_mod(int a, int b)
{
	return (a % b);
}

